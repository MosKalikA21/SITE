<p><b>3. Российская государственная библиотека</b></br>
	Находящаяся в городе Российская государственная библиотека — это крупнейшая публичная библиотека в России и континентальной Европе и одна из наибольших библиотек мира с фондом почти 45 млн книг на всевозможных языках. По количеству информативных материалов она занимает пятое место в мире после Библиотеки Конгресса в Вашингтоне, Британской библиотеки, Нью-Йоркской публичной библиотеки и Библиотеки и Архива Канады.</br>
