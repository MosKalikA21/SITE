   	 <p><b>1. Сталинские высотки</b></br>

В столице есть 7 очень похожих высотных зданий — воплощение идеи Сталина о превосходстве Советского Союза над всеми капиталистическими странами: 2 гостиницы, 2 жилых дома, 2 административных сооружения и университет. Этот архитектурный ансамбль хорошо известен нам как «сталинские высотки», а во многих англоязычных путеводителях его именуют Seven Sisters («семь сестер»). Оригинальный архитектурный стиль высоток получил в дальнейшем название «сталинский ампир».</br>
Кстати, заложены все эти здания были в один день — 7 сентября 1947 года, в честь 800-летия Москвы. На строительство ушло всего 10 лет. Возводить начали 8 высоток, но одна из них так и не была закончена из-за финансовых трудностей. Над созданием облика грандиозных построек трудились лучшие архитекторы СССР.
