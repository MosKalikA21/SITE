<p><b>4. McDonald’s на Пушкинской площади</b></br>
	Первый ресторан сети быстрого питания McDonald’s открылся в 1990 году на Пушкинской площади. Тогда это вызвало бешеный ажиотаж, и очередь к нему выстроилась на несколько километров. В первый день работы заведение на Пушкинской площади установило мировой рекорд первого рабочего дня в истории McDonald’s, обслужив более 30 тысяч посетителей. На тот момент этот ресторан являлся самым большим в мире (сегодня — самый большой в Европе). Регулярно занимает первую позицию в мировом рейтинге посещаемости McDonald’s.</p>
