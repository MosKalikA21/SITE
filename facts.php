<p><b>5. Московский метрополитен</b></br>
	Поезда Московского метрополитена ходят чаще любого другого метро мира. Интервал между составами в часы пик составляет полторы минуты. Среди них множество оригинальных именных поездов, подготовленных в рамках каких-либо событий, дат и акций.
Полная длина всех линий московского метро составляет 333,5 км. По ним курсирует около 10000 поездов. Многие из 200 станций признаны объектами культурного наследия, а все 12 станций кольцевой линии метро признаны памятниками архитектуры.
Кстати, станции по направлению к центру объявляются мужским голосом, а от центра — женским. На кольцевой линии метро мужской голос говорит при движении по часовой стрелке, а женский — против. Сделано это для того, чтобы помочь ориентироваться слепым пассажирам.</p>

