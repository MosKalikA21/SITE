<p><b>2. Третьяковский проезд</b></br>

Самая дорогая улица Москвы — Третьяковский проезд, где расположены наиболее известные и дорогие бутики мира. Еще в начале 2000-х годов компания Mercury, специализирующаяся на продаже товаров роскоши, задалась целью создать в этом месте собственный «ареал роскоши» и стала реставрировать Третьяковский проезд. Открытие первого магазина состоялось уже в 2001 году — начал работать бутик Giorgio Armani, который, к слову, является одним из наибольших в Европе по занимаемой площади. </p>

